/// Stage 3 — Algorithm 7.2.1.2P (plain changes; Steinhaus–Johnson–Trotter):
/// all `n!` permutations of `1, 2, ..., n`, each obtained from its predecessor
/// by one **adjacent** transposition.
///
/// Control tables: `c_j` records, odometer-fashion, how far element `j` has
